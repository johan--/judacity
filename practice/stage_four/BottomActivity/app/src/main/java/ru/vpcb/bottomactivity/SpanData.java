package ru.vpcb.bottomactivity;

public class SpanData {
        int spanX;
        int spanY;
        int width;
        int height;

        public SpanData(int spanX, int spanY, int width, int height) {
            this.spanX = spanX;
            this.spanY = spanY;
            this.width = width;
            this.height = height;
        }
    }